// ===== Quiz Logic =====
let currentQuestion = 0;
let score = 0;
let answered = false;

const questionNumberEl = document.getElementById("question-number");
const questionTextEl = document.getElementById("question-text");
const optionsContainerEl = document.getElementById("options-container");
const feedbackEl = document.getElementById("feedback");
const progressFillEl = document.getElementById("progress-fill");
const quizCardEl = document.getElementById("quiz-card");
const resultCardEl = document.getElementById("result-card");
const scoreTextEl = document.getElementById("score-text");
const scorePercentEl = document.getElementById("score-percent");
const restartBtnEl = document.getElementById("restart-btn");

function loadQuestion() {
  answered = false;
  feedbackEl.textContent = "";
  feedbackEl.className = "feedback";
  const q = questions[currentQuestion];

  questionNumberEl.textContent = `السؤال ${currentQuestion + 1} من ${questions.length}`;
  questionTextEl.textContent = q.question;
  optionsContainerEl.innerHTML = "";

  const letters = ["أ", "ب", "ج", "د"];

  q.options.forEach((optionText, index) => {
    const btn = document.createElement("button");
    btn.className = "option-btn";
    btn.innerHTML = `<span class="option-letter">${letters[index]}</span><span class="option-text">${optionText}</span>`;
    btn.addEventListener("click", () => selectAnswer(index, btn));
    optionsContainerEl.appendChild(btn);
  });

  const progressPercent = (currentQuestion / questions.length) * 100;
  progressFillEl.style.width = `${progressPercent}%`;
}

function selectAnswer(selectedIndex, btnEl) {
  if (answered) return;
  answered = true;

  const q = questions[currentQuestion];
  const allButtons = optionsContainerEl.querySelectorAll(".option-btn");

  allButtons.forEach((btn) => btn.classList.add("disabled"));

  if (selectedIndex === q.correct) {
    score++;
    btnEl.classList.add("correct");
    feedbackEl.textContent = "✅ إجابة صحيحة";
    feedbackEl.className = "feedback feedback-correct";
  } else {
    btnEl.classList.add("wrong");
    allButtons[q.correct].classList.add("correct");
    feedbackEl.textContent = "❌ إجابة خاطئة";
    feedbackEl.className = "feedback feedback-wrong";
  }

  setTimeout(() => {
    currentQuestion++;
    if (currentQuestion < questions.length) {
      loadQuestion();
    } else {
      showResult();
    }
  }, 1000);
}

function showResult() {
  quizCardEl.classList.add("hidden");
  resultCardEl.classList.remove("hidden");
  progressFillEl.style.width = "100%";

  const percent = Math.round((score / questions.length) * 100);
  scoreTextEl.textContent = `${score} من ${questions.length}`;
  scorePercentEl.textContent = `${percent}%`;

  let message = "";
  if (percent >= 90) message = "ممتاز! أنت جاهز للامتحان 🎉";
  else if (percent >= 70) message = "جيد جدًا، استمر بالمراجعة 👍";
  else if (percent >= 50) message = "لا بأس، تحتاج بعض المراجعة الإضافية 📚";
  else message = "يُنصح بمراجعة المادة جيدًا قبل الامتحان 💪";

  document.getElementById("result-message").textContent = message;
}

function restartQuiz() {
  currentQuestion = 0;
  score = 0;
  resultCardEl.classList.add("hidden");
  quizCardEl.classList.remove("hidden");
  loadQuestion();
}

restartBtnEl.addEventListener("click", restartQuiz);

loadQuestion();
